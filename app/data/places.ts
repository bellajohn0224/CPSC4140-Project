// ── Helpers ──────────────────────────────────────────────────────────────────

export function getPlacesByCategory(categoryId: string): Place[] {
  return places.filter(p => p.category === categoryId);
}

export function getPlaceById(id: string): Place | undefined {
  return places.find(p => p.id === id);
}

export function getFeaturedPlaces(section: FeaturedSection): Place[] {
  return places.filter(p => p.featured.includes(section));
}

export interface FilterOptions {
  query?: string;
  categories?: string[];
  isFree?: boolean;
  settings?: Setting[];
  dateBuckets?: DateBucket[];
  familyFriendly?: boolean;
  accessible?: boolean;
}

export type SortOption = 'name-az' | 'name-za' | 'free-first' | 'family-first';

export function applyFilters(all: Place[], opts: FilterOptions): Place[] {
  return all.filter(p => {
    if (opts.query) {
      const q = opts.query.toLowerCase();
      const hit =
        p.name.toLowerCase().includes(q) ||
        p.description.toLowerCase().includes(q) ||
        p.tags.some(t => t.toLowerCase().includes(q)) ||
        p.address.toLowerCase().includes(q) ||
        (p.date ?? '').toLowerCase().includes(q);
      if (!hit) return false;
    }
    if (opts.categories?.length && !opts.categories.includes(p.category)) return false;
    if (opts.isFree !== undefined && p.isFree !== opts.isFree) return false;
    if (opts.settings?.length && !opts.settings.includes(p.setting)) return false;
    if (opts.dateBuckets?.length) {
      if (!p.dateBucket || !opts.dateBuckets.includes(p.dateBucket)) return false;
    }
    if (opts.familyFriendly && !p.familyFriendly) return false;
    if (opts.accessible && !p.accessible) return false;
    return true;
  });
}

export function applySort(list: Place[], sort: SortOption): Place[] {
  const copy = [...list];
  switch (sort) {
    case 'name-az':      return copy.sort((a, b) => a.name.localeCompare(b.name));
    case 'name-za':      return copy.sort((a, b) => b.name.localeCompare(a.name));
    case 'free-first':   return copy.sort((a, b) => (a.isFree === b.isFree ? 0 : a.isFree ? -1 : 1));
    case 'family-first': return copy.sort((a, b) => (a.familyFriendly === b.familyFriendly ? 0 : a.familyFriendly ? -1 : 1));
    default:             return copy;
  }
}
